#Area of a circle

pi = 3.142
radius = 7
area = pi*radius*radius

print ("The area of a circle is" + str(area))


#Volume of a cube

length =3
volume = length*length*length

print ("The volume of a cube is" + str(volume))

#Volume of a cylinder

pi = 3.142
radius = 7
height = 4
vol = pi*radius*radius*height

print ("The volume of a cylinder is" + str(vol))

#Surface area of a cylinder

pi = 3.142
radius = 7
height =4
surface_area = 2*pi*radius*height + 2*pi*radius*radius

print ("The surface area of a cylinder is" + str(surface_area))
